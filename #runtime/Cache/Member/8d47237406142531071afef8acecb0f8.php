//					$(".video-cancle").hide();
//				}
            },
            FileUploaded:function (up, file, info) {
                var data = JSON.parse(info.response);
                if (info.status == 200){
                    $('#mybox').show();
                    $("#vurl").val(data.result)
                }
            },
            //上传失败回调
            Error: function(event, queueID, fileObj) {
                $(".suc-tip").text("文件上传失败!").addClass("red");
                $(".video-cancle").hide();
            },
        }
    });

